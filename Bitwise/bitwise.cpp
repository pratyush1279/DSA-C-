#include<iostream>
using namespace std;

int main(){
    int a = 10, b = 8;
    cout << (a | b) << endl;
    cout << (a & b) << endl;
    cout << (a ^ b) << endl;
    cout << (4 << 1) << endl;
    cout << sizeof(int)<< endl;
    cout << sizeof(long int)<< endl;
    cout << sizeof(short int)<< endl;
    cout << sizeof(long long)<< endl;
    cout << sizeof(signed)<< endl;
    cout << sizeof(unsigned)<< endl;
    return 0;
}